package doit;

public class Arithmetic {

	public static void main(String[] args) {
		int iRes; //intResult
		double dRes; //doubleResult

		iRes = 6+4;
		System.out.println(iRes);
		iRes = 6-4;
		System.out.println(iRes);
		iRes = 6*4;
		System.out.println(iRes);
		iRes = 6/4;
		System.out.println(iRes);
		dRes = 6.0/4.0;
		System.out.println(dRes);
		iRes = 6%4;
		System.out.println(iRes);

		dRes = (double)6/4;
		System.out.println(dRes);

	}

}
